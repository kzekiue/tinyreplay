/**
 * The dashboard's entire icon set: inline 24×24 stroke SVGs (path data after
 * lucide, ISC). No icon dependency - this file is the whole inventory, which
 * keeps the vocabulary consistent and the bundle tiny.
 */

type IconProps = SVGProps<SVGSVGElement> & { size?: number };

function icon(children: React.ReactNode) {
  return function Icon({ size = 16, ...props }: IconProps) {
    return (
      <svg
        width={size}
        height={size}
        viewBox="0 0 24 24"
        fill="none"
        stroke="currentColor"
        strokeWidth="2"
        strokeLinecap="round"
        strokeLinejoin="round"
        aria-hidden="true"
        {...props}
      >
        {children}
      </svg>
    );
  };
}

